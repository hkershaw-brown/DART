function  CompareMonthlyProfiles(month)

% crude script to compare the same month across years.
%
% USAGE (compare January of all years):
%    CompareMonthlyProfiles(1)

%% DART software - Copyright UCAR. This open source software is provided
% by UCAR, "as is", without charge, subject to all terms of use at
% http://www.image.ucar.edu/DAReS/DART/DART_download
%
% The obs_diag_output.nc files are archived in
% /glade/p/nsc/ncis0006/Reanalyses/f.e21.FHIST_BGC.f09_025.CAM6assim.011/ ...
%                esp/hist/YYYY-MM/Diags_NTrS_YYYY-MM.tgz
